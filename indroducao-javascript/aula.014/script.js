let diaDaSemana = Number(prompt("digite um numero de 1 - 7 para desconbrir o dia da semana"))

switch (diaDaSemana) {
    case 1:
        alert("domingo")
        break;
    case 2:
        alert("segunda-feira")
        break;
    case 3:
        alert("terça-feira")
        break;
    case 4:
        alert("quarta-feira")
        break;
    case 5:
        alert("quinta-feira")
        break;
    case 6:
        alert("sexta-feira")
        break;
    default:
        alert("nao jtem esse dia")
        break
}